package RubiksCube;

public class Point{
    private int X;
    private int Y;
    private int Z;

    public Point(int x, int y, int z){
        X = x;
        Y = y;
        Z = z;
    }

    public int getX(){
        return X;
    }
    public int getY(){
        return Y;
    }
    public int getZ(){
        return Z;
    }

}